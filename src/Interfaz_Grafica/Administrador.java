    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            try{
            String user = jTextField1.getText();
            String pass = jTextField2.getText();
            String query = "SELECT * FROM administrador WHERE correo='" + user + "' AND password ='" + pass + "'";
            
            Statement st = conn.conectar().createStatement();
            ResultSet rs = st.executeQuery(query);
            if(rs.next()){
                
                //con esta varibale vamos a guardar al usuario que inicio la sesion 
                int iduser = rs.getInt("id_administrador");
                sesion.setUserId(iduser);
                String nombre = rs.getString("nombre");
                
                
                
                JOptionPane.showMessageDialog(this, "Bienvenido " + nombre);
                dispose();
                java.awt.EventQueue.invokeLater(new Runnable() {
                    public void run() {
                        new Generar_Numero_Ganador_Admin().setVisible(true);
            }
        });     
            }else{
                JOptionPane.showMessageDialog(this, "Administradior no Validado");
            }
        } catch(SQLException ex){
            Logger.getLogger(Administrador.class.getName()).log(Level.SEVERE, null,ex);
        }          
    }//GEN-LAST:event_jButton1ActionPerformed

