    final void EnviarCorreo(){
      ControlRecuperacion recu = new ControlRecuperacion();
        recu.usuario = jTextField1.getText();
        try {
            try {
                ResultSet rs = recu.RecuperacionCorreo();
                while(rs.next()){
                    jTextField2.setText(rs.getString("correo"));
                    clave = rs.getString("password");
                    
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al capturar los datos");
            }
            
            Properties props = new Properties();
            props.setProperty("mail.smtp.host", "smtp.gmail.com");
            props.setProperty("mail.smtp.starttls.enable", "true");
            props.setProperty("mail.smtp.port", "587");
            props.setProperty("mail.smtp.ssl.protocols", "TLSv1.2");
            props.setProperty("mail.smtp.auth", "true");
            
            Session sess = Session.getDefaultInstance(props);
                
            String remitente = "soportemelate@gmail.com";
            String passRemitente = "khyjgqdxmpxocvfa";
            String receptor = jTextField2.getText();
            String asunto = "Recuperacion Password";
            String mensaje = "Tu password es" + clave;
            
            MimeMessage msj = new MimeMessage(sess);
            msj.setFrom(new InternetAddress(remitente));
            
            msj.addRecipient(Message.RecipientType.TO, new InternetAddress(receptor));
            msj.setSubject(asunto);
            msj.setText(mensaje);
            
            Transport t = sess.getTransport("smtp");
            t.connect(remitente, passRemitente);
            t.sendMessage(msj, msj.getRecipients(Message.RecipientType.TO));
            t.close();
            
            JOptionPane.showMessageDialog(null, "correo enviado");
        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error al enviar" + e.getMessage());
        }
    
    
    }

